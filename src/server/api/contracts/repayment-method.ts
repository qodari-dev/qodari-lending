import {
  CreateRepaymentMethodBodySchema,
  GetRepaymentMethodQuerySchema,
  ListRepaymentMethodsQuerySchema,
  UpdateRepaymentMethodBodySchema,
} from '@/schemas/repayment-method';
import { IdParamSchema } from '@/schemas/shared';
import { TsRestErrorSchema, TsRestMetaData } from '@/schemas/ts-rest';
import { Paginated } from '@/server/utils/query/schemas';

